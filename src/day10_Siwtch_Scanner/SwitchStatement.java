package day10_Siwtch_Scanner;

public class SwitchStatement {
    public static void main(String[] args) {

        switch (4){
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("invalid case");
                break;
        }



    }
}
